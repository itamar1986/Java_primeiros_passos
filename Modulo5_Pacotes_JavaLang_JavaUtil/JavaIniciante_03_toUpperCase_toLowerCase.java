package Modulo5_Pacotes_JavaLang_JavaUtil;
public class JavaIniciante_03_toUpperCase_toLowerCase 
{
	public static void main (String args[])
	{
		String s = "Java Iniciante";
		System.out.println("String s em maiúscula: " + s.toUpperCase());
		System.out.println("String s em minúscula: " + s.toLowerCase());
	}
}
/*
	Os métodos toUpperCase() e toLowerCase() são utilizados para transformar todas as 
	letras de uma determinada string em maiúsculas ou minúsculas, respectivamente.
*/