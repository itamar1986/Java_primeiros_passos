package Modulo5_Pacotes_JavaLang_JavaUtil;
public class JavaIniciante_02_charAt 
{
	public static void main (String args[])
	{
		String s = "Java Iniciante";
		System.out.println("Caracter da posição 7: " + s.charAt(6));
	}
}
/*
	O método charAt() é utilizado para retornar um caractere de uma determinada
	string de acordo com um índice especificado entre parênteses (parâmetro), sendo que
	o primeiro caractere tem o índice 0, o segundo tem o índice 1 e assim por diante.
*/